package com.entity;

public class Classify {
	private String classify_id;// 分类ID
	private String classify_name;// 分类名称
	private int classify_feight;// 权重

	public String getClassify_id() {
		return classify_id;
	}

	public void setClassify_id(String classify_id) {
		this.classify_id = classify_id;
	}

	public String getClassify_name() {
		return classify_name;
	}

	public void setClassify_name(String classify_name) {
		this.classify_name = classify_name;
	}

	public int getClassify_feight() {
		return classify_feight;
	}

	public void setClassify_feight(int classify_feight) {
		this.classify_feight = classify_feight;
	}

}
